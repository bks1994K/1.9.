int number = Convert.ToInt32(Console.ReadLine());
if (number > 0 && number < 10)
{
    Console.WriteLine("Число в диапазоне от 0 до 10");
}
else if (number > 20 && number < 30)
{
    Console.WriteLine("Число в диапазоне от 20 до 30");
}
else if (number > 40 && number < 50)
{
    Console.WriteLine("Число в диапазоне от 40 до 50");
}